    class DoDelayedLoad extends TimerTask  {
        public void run() {
            app.parseControl(webOS);
            String dev = app.getDeveloper();
            String descript = app.getDescription();
            if(dev==null) {
                dev = bundle.getString("UNKNOWN");
            }
            if(descript==null) {
                descript = bundle.getString("UNKNOWN");
            } else if(descript.equals("N/A")) {
                descript = bundle.getString("UNKNOWN");
            }
            jLabel10.setText("<html><p width=\"150\">" + dev + "</p>");
            jTextArea1.setText(removeHTML(descript));
        }
    }
