    private String removeHTML(String s) {
        s = s.replaceAll("</p>", "\n\n");
        s = s.replaceAll("<br>", "\n");
        s = s.replaceAll("<ul>", "\n");
        s = s.replaceAll("<ol>", "\n");
        s = s.replaceAll("<li>", "\t- ");
        s = s.replaceAll("</li>", "\n");
        s = s.replaceAll("\\<.*?\\>", "");
        s = s.replaceAll("&rsquo;", "'");
        s = s.replaceAll("&nbsp;", " ");
        s = s.replaceAll("&#58;", ":");
        s = s.replaceAll("&quot;", "\"");
        s = s.replaceAll("&amp;", "&");
        s = s.replaceAll("&lt;", "<");
        s = s.replaceAll("&gt;", ">");
        s = s.replaceAll("&raquo;", "\"");
        s = s.replaceAll("&laquo;", "\"");
        s = s.replaceAll("&raquo;", "\"");
        s = s.replaceAll("&copy;", "©");
        s = s.replaceAll("&reg;", "®");
        return s;
    }

